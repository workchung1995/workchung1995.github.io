function sample(array, size) {
    const results = [],
        sampled = {};
    while (results.length < size && results.length < array.length) {
        const index = Math.trunc(Math.random() * array.length);
        if (!sampled[index]) {
            results.push(array[index]);
            sampled[index] = true;
        }
    }
    return results;
}

function method1(sampleNames, classroom) {
    var i = 0
    for (var j = 0; j < classroom.length; j++) {
        for (var k = 0; k < classroom[j].length; k++) {
            if (classroom[j][k] === "S") {
                classroom[j][k] = (i < sampleNames.length) ? sampleNames[i] : "";
                i++
            } else {
                continue
            }
        }
    }

    return classroom;
}

